const int kTensorArenaSize = 4096;
alignas(16) uint8_t tensor_arena[kTensorArenaSize];

int main(int argc, char* argv[]) {
    printf("\r\n=== FPGA Functional Verification ===\r\n");

    tflite::MicroErrorReporter micro_error_reporter;
    const tflite::Model* model = tflite::GetModel(g_model);
    if (model->version() != TFLITE_SCHEMA_VERSION) return 1;

    tflite::MicroMutableOpResolver<6> resolver;
    resolver.AddFullyConnected();
    resolver.AddRelu();
    resolver.AddConv2D();
    resolver.AddDepthwiseConv2D();
    resolver.AddReshape();
    resolver.AddSoftmax();

    tflite::MicroInterpreter interpreter(model, resolver, tensor_arena, kTensorArenaSize);
    if (interpreter.AllocateTensors() != kTfLiteOk) {
        printf("Allocate Failed!\r\n");
        return 1;
    }

    TfLiteTensor* input = interpreter.input(0);
    TfLiteTensor* output = interpreter.output(0);

    if (sizeof(input_data) == input->bytes) {
        std::memcpy(input->data.int8, input_data, input->bytes);
    } else {
        printf("[Warn] Input data empty! Verification will likely fail.\r\n");
        std::memset(input->data.int8, 0, input->bytes);
    }

    printf("Running inference (No Timer mode)...\r\n");
    interpreter.Invoke();
    printf("Inference Done.\r\n");

    printf("Verifying...\r\n");
    int error_count = 0;
    int num_elements = output->bytes;

    for (int i = 0; i < num_elements; ++i) {
        int8_t hw = output->data.int8[i];
        int8_t gold = expected_output[i];
        
        int diff = hw - gold;
        if (diff < 0) diff = -diff; 

        if (diff > 2) {
            error_count++;
            if (error_count <= 5) {
                printf("[Fail] Idx %d: HW=%d, Golden=%d (Diff=%d)\r\n", i, hw, gold, hw - gold);
            }
        }
    }

    printf("\r\n======================================\r\n");
    if (error_count == 0) {
        printf("  [Status] ALL PASS! (Functional Success)\r\n");
        printf("  Note: Diff <= 2 is accepted for Soft-Math emulation.\r\n");
    } else {
        printf("  [Status] FAIL with %d errors.\r\n", error_count);
    }
    printf("======================================\r\n");

    while (1) {}
    return 0;
}